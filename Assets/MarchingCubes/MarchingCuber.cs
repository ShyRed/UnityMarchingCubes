    /// <summary>
    /// Generates the mesh
    /// </summary>
    public void GenerateMesh()
    {
        if (Voxeldata == null)
            throw new InvalidOperationException("Voxeldata is null!");

        int xSize = Voxeldata.GetLength(0);
        int ySize = Voxeldata.GetLength(1);
        int zSize = Voxeldata.GetLength(2);

        List<Vector3> vertices = new List<Vector3>();
        List<Vector2> uv = new List<Vector2>();
        List<int> triangles = new List<int>();

        Vector3[] points = new Vector3[xSize * ySize * zSize];
        int[] values = new int[xSize * ySize * zSize];
        int counter = 0;
        for (int z = 0; z < zSize; z++)
        {
            for (int y = 0; y < ySize; y++)
            {
                for (int x = 0; x < xSize; x++)
                {
                    points[counter] = new Vector3(
                        (x - xSize / 2f) * Scale,
                        (y - ySize / 2f) * Scale,
                        (z - zSize / 2f) * Scale);
                    values[counter] = Voxeldata[x, y, z];
                    counter++;
                }
            }
        }

        // Vertices may occur along edges of cube, when the values at the edge's endpoints
        // straddle the isolevel value.
        // Actual position along edge weighted according to function values.
        Vector3[] vlist = new Vector3[12];

        int vertexIndex = 0;

        for (int z = 0; z < zSize - 1; z++)
        {
            for (int y = 0; y < ySize - 1; y++)
            {
                for (int x = 0; x < xSize - 1; x++)
                {
                    // index of base point, and also adjacent points on cube
                    int p = x + xSize * y + xSize * ySize * z,
                        px = p + 1,
                        py = p + xSize,
                        pxy = py + 1,
                        pz = p + xSize * ySize,
                        pxz = px + xSize * ySize,
                        pyz = py + xSize * ySize,
                        pxyz = pxy + xSize * ySize;

                    // store scalar values corresponding to vertices
                    int value0 = values[p],
                        value1 = values[px],
                        value2 = values[py],
                        value3 = values[pxy],
                        value4 = values[pz],
                        value5 = values[pxz],
                        value6 = values[pyz],
                        value7 = values[pxyz];

                    int cubeindex = 0;
                    if (value0 != 0) cubeindex |= 1;
                    if (value1 != 0) cubeindex |= 2;
                    if (value2 != 0) cubeindex |= 8;
                    if (value3 != 0) cubeindex |= 4;
                    if (value4 != 0) cubeindex |= 16;
                    if (value5 != 0) cubeindex |= 32;
                    if (value6 != 0) cubeindex |= 128;
                    if (value7 != 0) cubeindex |= 64;

                    // bits = 12 bit number, indicates which edges are crossed by the isosurface
                    int bits = EdgeTable[cubeindex];

                    // if none are crossed, proceed to next iteration
                    if (bits == 0) continue;

                    // bottom of the cube
                    if ((bits & 1) != 0)
                        vlist[0] = points[p];
                    if ((bits & 2) != 0)
                        vlist[1] = points[px];
                    if ((bits & 4) != 0)
                        vlist[2] = points[py];
                    if ((bits & 8) != 0)
                        vlist[3] = points[p];
                    // top of the cube
                    if ((bits & 16) != 0)
                        vlist[4] = points[pz];
                    if ((bits & 32) != 0)
                        vlist[5] = points[pxz];
                    if ((bits & 64) != 0)
                        vlist[6] = points[pyz];
                    if ((bits & 128) != 0)
                        vlist[7] = points[pz];
                    // vertical lines of the cube
                    if ((bits & 256) != 0)
                        vlist[8] = points[p];
                    if ((bits & 512) != 0)
                        vlist[9] = points[px];
                    if ((bits & 1024) != 0)
                        vlist[10] = points[pxy];
                    if ((bits & 2048) != 0)
                        vlist[11] = points[py];

                    // construct triangles -- get correct vertices from triTable.
                    int i = 0;
                    cubeindex <<= 4;  // multiply by 16... 
                                      // "Re-purpose cubeindex into an offset into triTable." 
                                      //  since each row really isn't a row.

                    // the while loop should run at most 5 times,
                    //   since the 16th entry in each row is a -1.
                    while (TriTable[cubeindex + i] != -1)
                    {
                        int index3 = TriTable[cubeindex + i];
                        int index2 = TriTable[cubeindex + i + 1];
                        int index1 = TriTable[cubeindex + i + 2];

                        vertices.Add(vlist[index1]);
                        vertices.Add(vlist[index2]);
                        vertices.Add(vlist[index3]);

                        triangles.Add(vertexIndex);
                        triangles.Add(vertexIndex + 1);
                        triangles.Add(vertexIndex + 2);

                        uv.Add(new Vector2(0, 0));
                        uv.Add(new Vector2(0, 1));
                        uv.Add(new Vector2(1, 1));

                        vertexIndex += 3;
                        i += 3; 
                    }
                }
            }
        }

        // Set mesh
        MeshFilter meshFilter = GetComponent<MeshFilter>();
        Mesh mesh = meshFilter.mesh;
        if (mesh == null)
            mesh = new Mesh();
        else
            mesh.Clear();

        mesh.SetVertices(vertices);
        mesh.SetTriangles(triangles, 0);
        mesh.SetUVs(0, uv);
        mesh.RecalculateBounds();
        mesh.RecalculateNormals();

        Debug.Log("Vertices: " + mesh.vertices.Length);
        Debug.Log("Triangles: " + mesh.triangles.Length);
        Debug.Log("UV: " + mesh.uv.Length);

        meshFilter.mesh = mesh;
    }
